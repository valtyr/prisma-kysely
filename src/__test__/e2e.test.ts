const exec = promisify(execCb);

const writePrismaConfigFile = async (url: string) => {
  await fs.writeFile(
    "./prisma.config.ts",
    `// This file was generated by Prisma, and assumes you have installed the following:
// npm install --save-dev prisma dotenv
import "dotenv/config";
import { defineConfig } from "prisma/config";

export default defineConfig({
  schema: "prisma/schema.prisma",
  migrations: {
    path: "prisma/migrations",
  },
  datasource: {
    url: ${url},
  },
});
`
  );
};

// prisma is creating entries in .gitignore without looking inside if that entry
// exists already (in this case "src/generated/prisma"). This creates 13 extra
// entries, one per test run. We are just moving the real .gitignore out of the
// way for tests and replace it with the test generated one after all tests
// ran
beforeAll(async () => {
  await fs.rename("./.gitignore", "./.gitignore-old");
});

afterAll(async () => {
  await fs.rm("./.gitignore");
  await fs.rename("./.gitignore-old", "./.gitignore");
});

beforeEach(async () => {
  await fs.rename("./prisma", "./prisma-old").catch(() => {});
  await fs.rm("./generated/prisma", { force: true });
});

afterEach(async () => {
  await fs
    .rm("./prisma", {
      force: true,
      recursive: true,
    })
    .catch(() => {});
  await fs.rename("./prisma-old", "./prisma").catch(() => {});
  await fs.rm("./generated/prisma", { force: true });
  await fs.rm("./dev.db", { force: true });
  await fs.rm("./prisma.config.ts", { force: true });
});

test("End to end test", { timeout: 20000 }, async () => {
  // Initialize prisma:
  await exec("yarn prisma init --datasource-provider sqlite");

  await writePrismaConfigFile('"file:./dev.db"');

  // Set up a schema
  await fs.writeFile(
    "./prisma/schema.prisma",
    `datasource db {
        provider = "sqlite"
    }

    generator client {
      provider = "prisma-client"
      output   = "./generated/prisma"
    }

    generator kysely {
        provider  = "node ./dist/bin.js"
    }

    model TestUser {
        id          String @id
        name        String
        age         Int
        rating      Float
        updatedAt   DateTime
        sprockets   Sprocket[]
    }

    model Sprocket {
        id          Int @id
        users       TestUser[]
    }`
  );

  // Run Prisma commands without fail
  await exec("yarn prisma generate");

  const generatedSource = await fs.readFile("./prisma/generated/types.ts", {
    encoding: "utf-8",
  });

  expect(generatedSource).toContain(`export type SprocketToTestUser = {
    A: number;
    B: string;
};`);

  expect(generatedSource).toContain("_SprocketToTestUser: SprocketToTestUser");

  // Expect no Kysely wrapped types to be exported since we don't enable exportWrappedTypes
  expect(generatedSource).not.toContain("Insertable");
});

