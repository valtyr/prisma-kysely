test(
  "End to end test - SQLite with JSON support",
  { timeout: 20000 },
  async () => {
    await exec("yarn prisma init --datasource-provider sqlite");

    await writePrismaConfigFile('"file:./dev.db"');

    await fs.writeFile(
      "./prisma/schema.prisma",
      `
    datasource db {
        provider = "sqlite"
    }

    generator kysely {
        provider  = "node ./dist/bin.js"
    }
    
    generator client {
        provider = "prisma-client"
        output   = "./generated/prisma"
    }


    model TestUser {
        id          String   @id
        name        String
        metadata    Json     // JSON field supported in SQLite since Prisma 6.2
        preferences Json?    // Optional JSON field

        /// @kyselyType({ theme: 'light' | 'dark', language: string })
        settings    Json

        createdAt   DateTime @default(now())
    }

    model Product {
        id          Int      @id @default(autoincrement())
        name        String
        details     Json     // Product details as JSON

        /// @kyselyType(string[])
        tags        Json?    // Optional tags as JSON array
    }`
    );

    await exec("yarn prisma generate");

    const generatedSource = await fs.readFile("./prisma/generated/types.ts", {
      encoding: "utf-8",
    });

    expect(generatedSource).toContain(`export type TestUser = {
    id: string;
    name: string;
    metadata: unknown;
    preferences: unknown | null;
    /**
     * @kyselyType({ theme: 'light' | 'dark', language: string })
     */
    settings: { theme: 'light' | 'dark', language: string };
    createdAt: Generated<string>;
};`);

    expect(generatedSource).toContain(`export type Product = {
    id: Generated<number>;
    name: string;
    details: unknown;
    /**
     * @kyselyType(string[])
     */
    tags: string[] | null;
};`);

    expect(generatedSource).toContain(`export type DB = {
    Product: Product;
    TestUser: TestUser;
};`);
  }
);

test(
  "End to end test - multi-schema with views support",
  { timeout: 20000 },
  async () => {
    // Initialize prisma:
    await exec("yarn prisma init --datasource-provider postgresql");

    await writePrismaConfigFile('process.env["TEST_DATABASE_URL"]');

    // Set up a schema with views in different schemas
    await fs.writeFile(
      "./prisma/schema.prisma",
      `
    generator kysely {
        provider        = "node ./dist/bin.js"
        previewFeatures = ["multiSchema", "views"]
    }
    
    generator client {
        provider = "prisma-client"
        output   = "./generated/prisma"
    }

    datasource db {
        provider = "postgresql"
        schemas  = ["public", "analytics"]
    }

    model User {
        id      Int    @id
        name    String
        email   String
        posts   Post[]

        @@schema("public")
    }

    model Post {
        id       Int    @id
        title    String
        content  String
        authorId Int
        author   User   @relation(fields: [authorId], references: [id])

        @@schema("public")
    }

    view UserStats {
        id        Int    @unique
        name      String
        postCount Int

        @@schema("analytics")
    }

    view PostSummary {
        id      Int    @unique
        title   String
        author  String

        @@schema("public")
    }`
    );

    await exec("yarn prisma generate");

    const typeFile = await fs.readFile("./prisma/generated/types.ts", {
      encoding: "utf-8",
    });

    // Verify that views are properly prefixed with their schema names
    expect(typeFile).toContain(`export type DB = {
    "analytics.UserStats": UserStats;
    Post: Post;
    PostSummary: PostSummary;
    User: User;
};`);

    // Verify view types are generated correctly
    expect(typeFile).toContain(`export type UserStats = {
    id: number;
    name: string;
    postCount: number;
};`);

    expect(typeFile).toContain(`export type PostSummary = {
    id: number;
    title: string;
    author: string;
};`);
  }
);

